

def main():
    print('Python is a Calculator:')

    # Addition
    print('\nSum of numbers:')
    print(12 + 45)
    print(-12 + 45)
    print(3.24545 + 234.233 + 873 + -23423)
    print(985.726 + -2356.23 + 1.11)

    # Subtraction
    print('\nSub of numbers:')
    print(42 - 7)
    print(-42 - 7)
    print(-234.23 - 23.345 - 3.145)
    print(34 - -23.1224 - 84.23)

    # Multiplication
    print('\nMul of numbers:')
    print(7 * 12)
    print(12 * 12)
    print(12 * 3454 * -345.234)
    print(3.1415 * 3.1415 * -23.12 * -2.2)

    # Division
    print('\nDiv of numbers:')
    print(91 / 14)
    print(1024 / 8)
    print(345.23 / -235 )
